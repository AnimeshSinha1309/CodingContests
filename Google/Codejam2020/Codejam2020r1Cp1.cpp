const long long MOD = 1e9 + 7;

template <typename Type>
istream &operator>>(istream &in, vector<Type> &vec) {
    ll n = vec.size();
    for (int i = 0; i < n; i++)
        in >> vec[i];
    return in;
}
template <typename Type>
ostream &operator<<(ostream &out, vector<Type> &vec) {
    for (auto val : vec)
        out << val << " ";
    out << endl;
    return out;
}

int main() {
    ios_base::sync_with_stdio(false);
    cin.tie(nullptr);
    cout.tie(nullptr);
    int tests;
    cin >> tests;
    for (int test = 1; test <= tests; test++) {
        ll x, y;
        string m;
        cin >> x >> y >> m;
        int n = m.size();
        ll ans = -1;
        if (abs(x) + abs(y) == 0) {
            ans = 0;
        }
        for (int i = 0; i < n && ans == -1; i++) {
            switch (m[i]) {
                case 'N':
                    y++;
                    break;
                case 'S':
                    y--;
                    break;
                case 'E':
                    x++;
                    break;
                case 'W':
                    x--;
                    break;
            }
            if (abs(x) + abs(y) <= (i + 1)) {
                ans = i + 1;
            }
        }
        if (ans == -1)
            cout << "Case #" << test << ": IMPOSSIBLE" << endl;
        else
            cout << "Case #" << test << ": " << ans << endl;
    }
}